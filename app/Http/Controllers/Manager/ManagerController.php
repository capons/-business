    {
        $manager_task = DB::table('users')
            ->join('scripts', 'users.parent_id', '=', 'scripts.users_id')
            ->select('scripts.name','scripts.desc')
            ->where ('users.id', '=' , Auth::user()->id)
            ->get();
        return view ('manager.index',['manager_task' => $manager_task]);
    }

